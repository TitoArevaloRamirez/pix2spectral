"""
Improved generator model for pix2spectral.

Main additions relative to the original generator_model.py:
  - optional segmented PROSPECT-D generation over configurable wavelength ranges
  - optional small learned residual spectrum to capture shapes not generated by PROSPECT-D
  - wavelength-grid and spectral-segment utilities
  - faster CNN patch encoder option
  - GroupNorm/InstanceNorm/BatchNorm factory instead of BatchNorm-only blocks
  - attention + statistics pooling over leaf patches
  - optional shared or separate band encoders
  - vectorized forward_batch_list over variable patch counts per sample/band

Scientific note:
  In segmented mode, the model predicts one PROSPECT-D parameter vector per
  spectral segment. These segment-wise parameters are useful as physics-guided
  latent variables, but they should not be interpreted as one physically valid
  whole-leaf PROSPECT-D parameter set.
"""

from __future__ import annotations

from typing import Dict, Iterable, List, Optional, Sequence, Tuple

import numpy as np
import torch
import torch.nn as nn
import torch.nn.functional as F
from pyPro4Sail import prospect_jacobian


DEFAULT_BANDS = ["blue", "green", "red", "nir", "red_edge"]
DEFAULT_SPECTRAL_SEGMENTS = [
    (400.0, 900.0),
    (900.0, 1000.0),
    (1000.0, 2000.0),
    (2000.0, 2500.0),
]


