class MeanPool(nn.Module):
    out_dim: Optional[int] = None

    def forward(self, E: torch.Tensor) -> torch.Tensor:
        if E.numel() == 0:
            return torch.zeros(E.shape[1], device=E.device, dtype=E.dtype)
        return E.mean(dim=0)


class AttentionStatsPool(nn.Module):
    """
    Pool variable patch embeddings into one fixed-size band descriptor.

    Uses:
      - attention-weighted mean
      - standard deviation across patches
      - max across patches

    Then projects [mean, std, max] back to embed_dim.
    """

    def __init__(self, embed_dim: int, hidden_dim: Optional[int] = None):
        super().__init__()
        embed_dim = int(embed_dim)
        if hidden_dim is None:
            hidden_dim = max(16, embed_dim // 2)

        self.attn = nn.Sequential(
            nn.Linear(embed_dim, hidden_dim),
            nn.Tanh(),
            nn.Linear(hidden_dim, 1),
        )
        self.proj = nn.Sequential(
            nn.Linear(embed_dim * 3, embed_dim),
            nn.LayerNorm(embed_dim),
            nn.ReLU(inplace=True),
        )

    def forward(self, E: torch.Tensor) -> torch.Tensor:
        if E.numel() == 0:
            return torch.zeros(self.proj[0].out_features, device=E.device, dtype=E.dtype)

        if E.shape[0] == 1:
            mean = E[0]
            std = torch.zeros_like(mean)
            maxv = E[0]
        else:
            logits = self.attn(E).squeeze(-1)
            weights = torch.softmax(logits, dim=0).unsqueeze(-1)
            mean = torch.sum(weights * E, dim=0)
            std = torch.std(E, dim=0, unbiased=False)
            maxv = torch.max(E, dim=0).values

        return self.proj(torch.cat([mean, std, maxv], dim=0))


def build_pool(pooling_type: str, embed_dim: int) -> nn.Module:
    pooling_type = str(pooling_type).lower()
    if pooling_type in ["mean", "avg"]:
        return MeanPool()
    if pooling_type in ["attention_stats", "attn_stats", "attention"]:
        return AttentionStatsPool(embed_dim=embed_dim)
    raise ValueError(f"Unknown pooling_type={pooling_type}")


