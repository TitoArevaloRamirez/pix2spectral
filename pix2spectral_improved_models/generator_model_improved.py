class MultiSpectralPatchToProspectGenerator(nn.Module):
    """
    Multispectral patch generator with optional segmented PROSPECT-D heads.

    Input per sample:
        band_patches dict:
            "blue":     Tensor [N_blue, 1, H, W]
            "green":    Tensor [N_green, 1, H, W]
            ...

    Output per sample:
        y_fake:
            Tensor [L]

        pParams:
            Tensor [7] in full-spectrum mode
            Tensor [num_segments, 7] in segmented mode
    """

    def __init__(
        self,
        bands: Optional[Sequence[str]] = None,
        base_features: int = 8,
        embed_dim: int = 64,
        mins=None,
        maxs=None,
        wavelength_min: float = 400.0,
        wavelength_max: float = 2500.0,
        wavelength_count: int = 2101,
        spectral_segments: Optional[Sequence[Tuple[float, float]]] = None,
        use_segmented_prospect: bool = True,
        use_segment_residual: bool = True,
        segment_residual_scale: float = 0.05,
        output_clamp: Optional[Tuple[float, float]] = (0.0, 1.0),
        patch_encoder_type: str = "cnn",
        pooling_type: str = "attention_stats",
        band_encoder_mode: str = "shared",
        norm_type: str = "group",
        mlp_hidden_dims: Sequence[int] = (256, 128, 64),
    ):
        super().__init__()

        if bands is None:
            bands = DEFAULT_BANDS
        self.bands = list(bands)

        self.wavelength_min = float(wavelength_min)
        self.wavelength_max = float(wavelength_max)
        self.wavelength_count = int(wavelength_count)
        self.use_segmented_prospect = bool(use_segmented_prospect)
        self.use_segment_residual = bool(use_segment_residual)
        self.segment_residual_scale = float(segment_residual_scale)
        self.output_clamp = output_clamp
        self.embed_dim = int(embed_dim)

        wavelengths_np = make_wavelengths_from_values(
            self.wavelength_min,
            self.wavelength_max,
            self.wavelength_count,
        )
        self.register_buffer(
            "wavelengths",
            torch.as_tensor(wavelengths_np, dtype=torch.float32),
            persistent=False,
        )

        if self.use_segmented_prospect:
            if spectral_segments is None:
                spectral_segments = DEFAULT_SPECTRAL_SEGMENTS
        else:
            spectral_segments = [(self.wavelength_min, self.wavelength_max)]

        self.spectral_segments = normalize_segments(
            spectral_segments,
            self.wavelength_min,
            self.wavelength_max,
        )
        self.num_segments = len(self.spectral_segments)

        idx_np = spectral_segment_indices(wavelengths_np, self.spectral_segments)
        self._segment_buffer_names: List[str] = []
        for i, idx in enumerate(idx_np):
            name = f"_segment_idx_{i}"
            self.register_buffer(
                name,
                torch.as_tensor(idx, dtype=torch.long),
                persistent=False,
            )
            self._segment_buffer_names.append(name)

        boundaries = boundary_indices_from_segments(wavelengths_np, self.spectral_segments)
        self.register_buffer(
            "boundary_indices",
            torch.as_tensor(boundaries, dtype=torch.long),
            persistent=False,
        )

        # Encoders: shared or one encoder per image band.
        band_encoder_mode = str(band_encoder_mode).lower()
        self.band_encoder_mode = band_encoder_mode

        if band_encoder_mode == "shared":
            self.patch_encoder = build_patch_encoder(
                encoder_type=patch_encoder_type,
                in_channels=1,
                base_features=base_features,
                embed_dim=embed_dim,
                norm_type=norm_type,
            )
            self.patch_encoders = None
        elif band_encoder_mode in ["separate", "per_band", "independent"]:
            self.patch_encoder = None
            self.patch_encoders = nn.ModuleDict(
                {
                    b: build_patch_encoder(
                        encoder_type=patch_encoder_type,
                        in_channels=1,
                        base_features=base_features,
                        embed_dim=embed_dim,
                        norm_type=norm_type,
                    )
                    for b in self.bands
                }
            )
        else:
            raise ValueError(
                f"Unknown band_encoder_mode={band_encoder_mode}. "
                "Expected 'shared' or 'separate'."
            )

        self.pool = nn.ModuleDict({b: build_pool(pooling_type, embed_dim) for b in self.bands})

        fused_dim = embed_dim * len(self.bands)

        mlp_layers: List[nn.Module] = []
        in_dim = fused_dim
        for h in mlp_hidden_dims:
            mlp_layers.extend(
                [
                    nn.Linear(in_dim, int(h)),
                    nn.LayerNorm(int(h)),
                    nn.ReLU(inplace=True),
                ]
            )
            in_dim = int(h)

        # One 7-parameter vector per segment in segmented mode.
        mlp_layers.append(nn.Linear(in_dim, self.num_segments * 7))
        self.param_mlp = nn.Sequential(*mlp_layers)

        self.residual_mlp: Optional[nn.Module]
        if self.use_segment_residual:
            self.residual_mlp = nn.Sequential(
                nn.Linear(fused_dim, max(128, embed_dim * 2)),
                nn.LayerNorm(max(128, embed_dim * 2)),
                nn.ReLU(inplace=True),
                nn.Linear(max(128, embed_dim * 2), self.wavelength_count),
                nn.Tanh(),
            )
        else:
            self.residual_mlp = None

        self.physics = PhysicsInformedProspectHead(mins=mins, maxs=maxs)

    def segment_indices(self) -> List[torch.Tensor]:
        return [getattr(self, name) for name in self._segment_buffer_names]

    def _encoder_for_band(self, band_name: str) -> nn.Module:
        if self.band_encoder_mode == "shared":
            assert self.patch_encoder is not None
            return self.patch_encoder
        assert self.patch_encoders is not None
        return self.patch_encoders[band_name]

    def _encode_single_sample(self, band_patches: Dict[str, torch.Tensor]) -> torch.Tensor:
        """Return fused descriptor [1, fused_dim] for one sample."""
        vecs = []
        for b in self.bands:
            P = band_patches[b]
            E = self._encoder_for_band(b)(P)
            v = self.pool[b](E)
            vecs.append(v)
        fused = torch.cat(vecs, dim=0).unsqueeze(0)
        return fused

    def _encode_batch_list(self, batch_band_dict: Dict[str, List[torch.Tensor]]) -> torch.Tensor:
        """
        Efficiently encode a collated batch where each band maps to a list of
        variable-length patch tensors.

        Returns:
            fused descriptors [B, len(bands) * embed_dim]
        """
        B = len(batch_band_dict[self.bands[0]])
        band_descriptors: List[torch.Tensor] = []

        for b in self.bands:
            patch_list = batch_band_dict[b]
            lengths = [int(p.shape[0]) for p in patch_list]

            if sum(lengths) == 0:
                device = next(self.parameters()).device
                dtype = next(self.parameters()).dtype
                pooled = torch.zeros(B, self.embed_dim, device=device, dtype=dtype)
                band_descriptors.append(pooled)
                continue

            all_patches = torch.cat(patch_list, dim=0)
            all_embeddings = self._encoder_for_band(b)(all_patches)
            split_embeddings = torch.split(all_embeddings, lengths, dim=0)

            pooled_list = [self.pool[b](E_i) for E_i in split_embeddings]
            band_descriptors.append(torch.stack(pooled_list, dim=0))

        return torch.cat(band_descriptors, dim=1)

    def _assemble_segmented_spectrum(
        self,
        prospect_full: torch.Tensor,
    ) -> torch.Tensor:
        """
        Assemble final [B,L] spectrum from full PROSPECT curves per segment.

        prospect_full:
            [B, S, L]
        """
        if prospect_full.dim() != 3:
            raise ValueError(f"Expected prospect_full [B,S,L], got {prospect_full.shape}")

        pieces = []
        for i, idx in enumerate(self.segment_indices()):
            idx = idx.to(prospect_full.device)
            pieces.append(prospect_full[:, i, idx])

        return torch.cat(pieces, dim=1)

    def forward_from_fused(self, fused: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor]:
        """
        Convert fused patch descriptors to spectrum and PROSPECT parameters.

        Args:
            fused: Tensor [B, fused_dim]

        Returns:
            y_fake:
                Tensor [B, L]

            p_params:
                Tensor [B, 7] in full mode
                Tensor [B, S, 7] in segmented mode
        """
        B = fused.shape[0]
        raw_params = self.param_mlp(fused).view(B, self.num_segments, 7)

        raw_flat = raw_params.reshape(B * self.num_segments, 7)
        prospect_flat, p_flat = self.physics(raw_flat)

        # pyPro4Sail returns 2101 values for 400-2500 nm in the usual case.
        if prospect_flat.shape[1] != self.wavelength_count:
            raise RuntimeError(
                f"PROSPECT returned length {prospect_flat.shape[1]}, but "
                f"wavelength_count={self.wavelength_count}. Check wavelength grid."
            )

        prospect_full = prospect_flat.view(B, self.num_segments, self.wavelength_count)
        p_params = p_flat.view(B, self.num_segments, 7)

        y_fake = self._assemble_segmented_spectrum(prospect_full)

        if self.residual_mlp is not None:
            residual = self.segment_residual_scale * self.residual_mlp(fused)
            if residual.shape[1] != y_fake.shape[1]:
                raise RuntimeError(
                    f"Residual length {residual.shape[1]} does not match y_fake "
                    f"length {y_fake.shape[1]}."
                )
            y_fake = y_fake + residual

        if self.output_clamp is not None:
            lo, hi = self.output_clamp
            y_fake = torch.clamp(y_fake, float(lo), float(hi))

        if not self.use_segmented_prospect and p_params.shape[1] == 1:
            p_params = p_params[:, 0, :]

        return y_fake, p_params

    def segment_boundary_loss(self, y: torch.Tensor, reduction: str = "mean") -> torch.Tensor:
        """
        Penalize discontinuities at spectral segment boundaries.

        Args:
            y: spectrum [B,L] or [L]
        """
        if self.boundary_indices.numel() == 0:
            return torch.zeros((), device=y.device, dtype=y.dtype)

        if y.dim() == 1:
            y = y.unsqueeze(0)

        losses = []
        for idx in self.boundary_indices.to(y.device):
            i = int(idx.item())
            if i <= 0 or i >= y.shape[1]:
                continue
            jump = y[:, i] - y[:, i - 1]
            losses.append(jump.pow(2))

        if len(losses) == 0:
            return torch.zeros((), device=y.device, dtype=y.dtype)

        out = torch.stack(losses, dim=1)
        if reduction == "mean":
            return out.mean()
        if reduction == "sum":
            return out.sum()
        return out

    def forward(self, band_patches: Dict[str, torch.Tensor]) -> Tuple[torch.Tensor, torch.Tensor]:
        fused = self._encode_single_sample(band_patches)
        y_fake, p_params = self.forward_from_fused(fused)

        y_fake = y_fake.squeeze(0)
        if p_params.dim() == 3:
            p_params = p_params.squeeze(0)
        else:
            p_params = p_params.squeeze(0)

        return y_fake, p_params

    def forward_batch_list(
        self,
        batch_band_dict: Dict[str, List[torch.Tensor]],
    ) -> Tuple[torch.Tensor, torch.Tensor]:
        fused = self._encode_batch_list(batch_band_dict)
        return self.forward_from_fused(fused)


