class ConvBlock2D(nn.Module):
    def __init__(
        self,
        in_ch: int,
        out_ch: int,
        kernel_size: int = 3,
        stride: int = 1,
        norm_type: str = "group",
        activation: str = "leaky",
    ):
        super().__init__()
        padding = kernel_size // 2
        layers: List[nn.Module] = [
            nn.Conv2d(
                in_ch,
                out_ch,
                kernel_size=kernel_size,
                stride=stride,
                padding=padding,
                padding_mode="reflect",
                bias=False,
            ),
            norm2d(out_ch, norm_type),
        ]
        if activation == "leaky":
            layers.append(nn.LeakyReLU(0.2, inplace=True))
        elif activation == "relu":
            layers.append(nn.ReLU(inplace=True))
        else:
            raise ValueError(f"Unknown activation={activation}")
        self.net = nn.Sequential(*layers)

    def forward(self, x):
        return self.net(x)


class PatchCNNEncoder(nn.Module):
    """
    Faster CNN patch encoder.

    Input:
        [N, 1, H, W], typically [N, 1, 32, 32]
    Output:
        [N, embed_dim]

    Unlike the previous U-Net-like encoder, this does not upsample back to the
    original resolution because the task only needs patch embeddings.
    """

    def __init__(
        self,
        in_channels: int = 1,
        base_features: int = 16,
        embed_dim: int = 64,
        norm_type: str = "group",
    ):
        super().__init__()
        f = int(base_features)

        self.net = nn.Sequential(
            ConvBlock2D(in_channels, f, kernel_size=3, stride=1, norm_type=norm_type),
            ConvBlock2D(f, f * 2, kernel_size=4, stride=2, norm_type=norm_type),
            ConvBlock2D(f * 2, f * 4, kernel_size=4, stride=2, norm_type=norm_type),
            ConvBlock2D(f * 4, f * 8, kernel_size=4, stride=2, norm_type=norm_type),
            ConvBlock2D(f * 8, f * 8, kernel_size=4, stride=2, norm_type=norm_type),
            nn.AdaptiveAvgPool2d(1),
            nn.Flatten(),
        )

        self.proj = nn.Sequential(
            nn.Linear(f * 8, embed_dim),
            nn.LayerNorm(embed_dim),
            nn.ReLU(inplace=True),
        )

    def forward(self, x):
        return self.proj(self.net(x))


class Down(nn.Module):
    def __init__(self, in_ch: int, out_ch: int, norm_type: str = "group"):
        super().__init__()
        self.net = nn.Sequential(
            nn.Conv2d(
                in_ch,
                out_ch,
                kernel_size=4,
                stride=2,
                padding=1,
                padding_mode="reflect",
                bias=False,
            ),
            norm2d(out_ch, norm_type),
            nn.LeakyReLU(0.2, inplace=True),
        )

    def forward(self, x):
        return self.net(x)


class Up(nn.Module):
    def __init__(
        self,
        in_ch: int,
        out_ch: int,
        norm_type: str = "group",
        use_dropout: bool = False,
    ):
        super().__init__()
        layers: List[nn.Module] = [
            nn.ConvTranspose2d(
                in_ch, out_ch, kernel_size=4, stride=2, padding=1, bias=False
            ),
            norm2d(out_ch, norm_type),
            nn.ReLU(inplace=True),
        ]
        if use_dropout:
            layers.append(nn.Dropout(0.5))
        self.net = nn.Sequential(*layers)

    def forward(self, x):
        return self.net(x)


class SmallUNetPatchEncoder(nn.Module):
    """
    Legacy U-Net style encoder for 32x32 patches.

    Kept for compatibility. The default recommended encoder is PatchCNNEncoder.
    """

    def __init__(
        self,
        in_channels: int = 1,
        base_features: int = 8,
        embed_dim: int = 64,
        norm_type: str = "group",
    ):
        super().__init__()
        f = int(base_features)

        self.initial = nn.Sequential(
            nn.Conv2d(
                in_channels,
                f,
                kernel_size=3,
                stride=1,
                padding=1,
                padding_mode="reflect",
                bias=False,
            ),
            norm2d(f, norm_type),
            nn.LeakyReLU(0.2, inplace=True),
        )

        self.d1 = Down(f, f * 2, norm_type=norm_type)
        self.d2 = Down(f * 2, f * 4, norm_type=norm_type)
        self.d3 = Down(f * 4, f * 8, norm_type=norm_type)
        self.d4 = Down(f * 8, f * 8, norm_type=norm_type)
        self.d5 = Down(f * 8, f * 8, norm_type=norm_type)

        self.u1 = Up(f * 8, f * 8, norm_type=norm_type)
        self.u2 = Up(f * 8 + f * 8, f * 8, norm_type=norm_type)
        self.u3 = Up(f * 8 + f * 8, f * 4, norm_type=norm_type)
        self.u4 = Up(f * 4 + f * 4, f * 2, norm_type=norm_type)
        self.u5 = Up(f * 2 + f * 2, f, norm_type=norm_type)

        self.fuse = nn.Sequential(
            nn.Conv2d(
                f + f,
                f,
                kernel_size=3,
                stride=1,
                padding=1,
                padding_mode="reflect",
                bias=False,
            ),
            norm2d(f, norm_type),
            nn.ReLU(inplace=True),
        )

        self.proj = nn.Sequential(
            nn.Linear(f, embed_dim),
            nn.LayerNorm(embed_dim),
            nn.ReLU(inplace=True),
        )

    def forward(self, x):
        x0 = self.initial(x)
        x1 = self.d1(x0)
        x2 = self.d2(x1)
        x3 = self.d3(x2)
        x4 = self.d4(x3)
        x5 = self.d5(x4)

        y1 = self.u1(x5)
        y2 = self.u2(torch.cat([y1, x4], 1))
        y3 = self.u3(torch.cat([y2, x3], 1))
        y4 = self.u4(torch.cat([y3, x2], 1))
        y5 = self.u5(torch.cat([y4, x1], 1))

        z = self.fuse(torch.cat([y5, x0], 1))
        z = z.mean(dim=(2, 3))
        z = self.proj(z)
        return z


def build_patch_encoder(
    encoder_type: str,
    in_channels: int,
    base_features: int,
    embed_dim: int,
    norm_type: str,
) -> nn.Module:
    encoder_type = str(encoder_type).lower()
    if encoder_type in ["cnn", "fast_cnn", "patch_cnn"]:
        return PatchCNNEncoder(
            in_channels=in_channels,
            base_features=base_features,
            embed_dim=embed_dim,
            norm_type=norm_type,
        )
    if encoder_type in ["unet", "small_unet", "legacy_unet"]:
        return SmallUNetPatchEncoder(
            in_channels=in_channels,
            base_features=base_features,
            embed_dim=embed_dim,
            norm_type=norm_type,
        )
    raise ValueError(f"Unknown patch encoder type: {encoder_type}")


